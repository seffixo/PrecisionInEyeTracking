def project_3d_to_2d(
    points_3d,
    camera_matrix,
    distortion_coeffs,
    rotation_vector,
    translation_vector,
    normalize_coordinates=False,
    image_size=None
):
    """
    Projects 3D world coordinates to 2D image coordinates using camera parameters.
    
    Parameters:
    -----------
    points_3d : array-like
        3D points in world coordinates. Shape: (N, 3) or (3,) for single point
    camera_matrix : array-like
        3x3 camera intrinsic matrix
    distortion_coeffs : array-like
        Distortion coefficients [k1, k2, p1, p2, k3, ...]
    rotation_vector : array-like
        3x1 rotation vector (rvec) from world to camera coordinates
    translation_vector : array-like
        3x1 translation vector (tvec) from world to camera coordinates
    normalize_coordinates : bool, optional
        If True, return normalized coordinates [0, 1]. Default: False
    image_size : tuple, optional
        (width, height) of image. Required if normalize_coordinates=True
        
    Returns:
    --------
    numpy.ndarray
        2D image coordinates. Shape: (N, 2) or (2,) for single point
        If normalize_coordinates=True, coordinates are in range [0, 1]
        Otherwise, coordinates are in pixels
    """
    
    # Convert inputs to numpy arrays
    points_3d = np.array(points_3d, dtype=np.float32)
    camera_matrix = np.array(camera_matrix, dtype=np.float32)
    distortion_coeffs = np.array(distortion_coeffs, dtype=np.float32)
    rotation_vector = np.array(rotation_vector, dtype=np.float32)
    translation_vector = np.array(translation_vector, dtype=np.float32)
    
    # Handle single point input
    single_point = False
    if points_3d.ndim == 1:
        points_3d = points_3d.reshape(1, -1)
        single_point = True
    
    # Ensure points_3d has correct shape
    if points_3d.shape[1] != 3:
        raise ValueError("points_3d must have shape (N, 3) or (3,)")
    
    # Project 3D points to 2D image coordinates
    image_points, _ = cv2.projectPoints(
        points_3d,
        rotation_vector,
        translation_vector,
        camera_matrix,
        distortion_coeffs
    )
    
    # Reshape result to (N, 2)
    image_points = image_points.reshape(-1, 2)
    
    # Normalize coordinates if requested
    if normalize_coordinates:
        if image_size is None:
            raise ValueError("image_size must be provided when normalize_coordinates=True")
        width, height = image_size
        image_points[:, 0] /= width   # x coordinates
        image_points[:, 1] /= height  # y coordinates
    
    # Return single point as 1D array if input was single point
    if single_point:
        return image_points[0]
    
    return image_points


