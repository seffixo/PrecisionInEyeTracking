image_size = (1920, 1080)

# --- ENDE DER DATENEINGABE ---


def calibrate_camera(obj_points_data, norm_img_points_data, img_size):
    """
    F√ºhrt die Kamerakalibrierung durch, um die intrinsische Matrix und die
    Verzerrungskoeffizienten zu finden.
    """
    if len(obj_points_data) < 6:
        print("Fehler: Es werden mindestens 6 Punkte f√ºr die Kalibrierung ben√∂tigt.")
        return

    width, height = img_size
    image_points_pixel = np.array(
        [[p[0] * width, p[1] * height] for p in norm_img_points_data],
        dtype=np.float32,
    )

    object_points = [np.array(obj_points_data, dtype=np.float32)]
    image_points = [image_points_pixel]

    print("Starte Kamerakalibrierung...")
    print(f"Verwende {len(obj_points_data)} Punktkorrespondenzen.")
    print(f"Bildgr√∂√üe: {img_size[0]}x{img_size[1]} Pixel.")
    print("-" * 30)

    # --- NEU: Erstelle eine initiale Sch√§tzung f√ºr die Kameramatrix ---
    # Der Hauptpunkt (cx, cy) wird als Bildzentrum angenommen.
    # Die Brennweite (fx, fy) wird als Bildbreite angenommen.
    initial_camera_matrix = np.array(
        [
            [float(width), 0, width / 2],
            [0, float(width), height / 2],
            [0, 0, 1],
        ],
        dtype=np.float32,
    )
    print("Verwende initiale Sch√§tzung f√ºr die Kameramatrix (non-planar rig):")
    print(initial_camera_matrix)
    print("-" * 30)
    # --------------------------------------------------------------------

    # --- NEU: F√ºge die initiale Matrix und das Flag hinzu ---
    # Wir √ºbergeben die Sch√§tzung und das Flag CALIB_USE_INTRINSIC_GUESS
    flags = cv2.CALIB_USE_INTRINSIC_GUESS
    ret, mtx, dist, rvecs, tvecs = cv2.calibrateCamera(
        object_points, image_points, img_size, initial_camera_matrix, None, flags=flags
    )

    if not ret:
        print("Kalibrierung fehlgeschlagen. √úberpr√ºfe deine Punkte.")
        return

    print("Kalibrierung erfolgreich!\n")

    print("RMS Reprojektionsfehler:")
    print(f"{ret:.4f} Pixel")
    print(
        "(Ein guter Wert liegt typischerweise unter 1.0, idealerweise nahe 0.)\n"
    )

    print("Finale Kameramatrix (Intrinsics) K:")
    print(mtx)
    print(f"  -> fx = {mtx[0, 0]:.2f} (Brennweite x)")
    print(f"  -> fy = {mtx[1, 1]:.2f} (Brennweite y)")
    print(f"  -> cx = {mtx[0, 2]:.2f} (Hauptpunkt x)")
    print(f"  -> cy = {mtx[1, 2]:.2f} (Hauptpunkt y)\n")

    print("Verzerrungskoeffizienten (Distortion Coefficients) D:")
    print(dist)
    if dist is not None and len(dist[0]) >= 5:
        print(f"  -> k1 = {dist[0, 0]:.6f} (radial)")
        print(f"  -> k2 = {dist[0, 1]:.6f} (radial)")
        print(f"  -> p1 = {dist[0, 2]:.6f} (tangential)")
        print(f"  -> p2 = {dist[0, 3]:.6f} (tangential)")
        print(f"  -> k3 = {dist[0, 4]:.6f} (radial)\n")

    print("Extrinsische Parameter (Kamera-Pose):")
    print(f"Rotationsvektor (rvec):")
    print(rvecs[0])
    print(f"\nTranslationsvektor (tvec) in mm:")
    print(tvecs[0])


